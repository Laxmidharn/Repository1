import java.util.Scanner;

class  SumOfNum
{
	public static int sum(int i)
	{
		if(i==0) return 0;
		return i+sum(i-1);
	}
	public static void main(String[] args) 
	{
		System.out.println("Enter a nummber to check the sum");
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		System.out.println(sum(n));
	}
}
